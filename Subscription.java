public class Subscription
{
    private int juniorPrice;
    private int seniorPrice;
    private int passivPrice;
    private int discount;

    public int getJuniorPrice()
    {
        return juniorPrice;
    }

    public void setJuniorPrice(int juniorPrice)
    {
        this.juniorPrice = juniorPrice;
    }

    public int getSeniorPrice()
    {
        return seniorPrice;
    }

    public void setSeniorPrice(int seniorPrice)
    {
        this.seniorPrice = seniorPrice;
    }

    public int getPassivPrice()
    {
        return passivPrice;
    }

    public void setPassivPrice(int passivPrice)
    {
        this.passivPrice = passivPrice;
    }

    public int getDiscount()
    {
        return discount;
    }

    public void setDiscount(int discount)
    {
        this.discount = discount;
    }
}
